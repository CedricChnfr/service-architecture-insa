package fr.insa.tp.windowsManagement;

public class WindowAction {

    private String action;

    // Constructeur
    public WindowAction(String action) {
        this.action = action;
    }

    // Getter et setter
    public String getAction() {
        return action;
    }

    public void setAction(String action) {
        this.action = action;
    }
}
